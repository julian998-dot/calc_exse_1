    public static boolean existepunto(String c){
        boolean r;
        r=false;
        for(int i=0; i<c.length(); i++){
            if(c.substring(1,1+1).equals(".")){
                r=true;
                break;
            }
        }
        return r;
    }
    public static String SRMD(String mem1,String sim,String mem2){
        double r=0;
        String res;
        if(sim.equals("+")){
            
           r=(Double.parseDouble(mem1)+Double.parseDouble(mem2)); 
        }
        if(sim.equals("-")){
             r=(Double.parseDouble(mem1)-Double.parseDouble(mem2)); 
        }
        if(sim.equals("*")){
             r=(Double.parseDouble(mem1)*Double.parseDouble(mem2)); 
        }
        if(sim.equals("/")){
            if(!mem2.equals("0")){
             r=(Double.parseDouble(mem1)/Double.parseDouble(mem2)); }
            else{
                JOptionPane.showMessageDialog(null, "ERROR, DIVISION EN CERO");
            }
        }
        res=String.valueOf(r);
        return res;
    }
